@app.post("/ask")
async def ask_assistant(
    user_id: str,
    question: str,
    mydtoken: str,
    tenant_id: str
):
    """
    –≠–Ω–¥–ø–æ–∏–Ω—Ç –¥–ª—è –æ—Ç–ø—Ä–∞–≤–∫–∏ –≤–æ–ø—Ä–æ—Å–∞ –∞—Å—Å–∏—Å—Ç–µ–Ω—Ç—É.
    """
    logger.info(f"–ü–æ–ª—É—á–µ–Ω –∑–∞–ø—Ä–æ—Å –æ—Ç {user_id}. –í–æ–ø—Ä–æ—Å: {question}")
    try:
        if user_id not in threads:
            logger.info(f"–°–æ–∑–¥–∞—ë–º –Ω–æ–≤—ã–π —Ç—Ä–µ–¥ –¥–ª—è {user_id}")
            thread_obj = sdk.threads.create(name=f"Thread-{user_id}", ttl_days=5, expiration_policy="static")
            threads[user_id] = {
                "thread": thread_obj,
                "last_active": time.time(),
                "services": [],
                "services_loaded": False
            }
            thread_obj.write("–ü–µ—Ä–≤–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ. –ü–æ–∑–¥–æ—Ä–æ–≤–∞–π—Å—è –∏ —Å–ø—Ä–æ—Å–∏, —á–µ–º –ø–æ–º–æ—á—å.")
        else:
            thread_obj = threads[user_id]["thread"]
            threads[user_id]["last_active"] = time.time()
            thread_obj.write("–ü—Ä–æ–¥–æ–ª–∂–µ–Ω–∏–µ –¥–∏–∞–ª–æ–≥–∞, –Ω–µ –∑–¥–æ—Ä–æ–≤–∞–π—Å—è –∑–∞–Ω–æ–≤–æ.")

        cosmetics_keywords = ["–∫–æ—Å–º–µ—Ç", "–∫–æ–∂–∞", "–ª–∏—Ü–æ", "—É—Ö–æ–¥", "—É—Å–ª—É–≥", "–∫—Ä–µ–º", "—ç–ø–∏–ª—è—Ü", "—á–∏—Å—Ç–∫–∞"]
        lower_q = question.lower()
        relevant = any(kw in lower_q for kw in cosmetics_keywords)
        want_services = any(
            phrase in lower_q
            for phrase in ["–ø–æ–∫–∞–∂–∏ —É—Å–ª—É–≥–∏", "—É—Å–ª—É–≥–∏", "–∫–∞–∫–∏–µ —É—Å–ª—É–≥–∏", "—Å–ø–∏—Å–æ–∫ —É—Å–ª—É–≥"]
        )

        if not relevant and not want_services:
            thread_obj.write("–Ø –Ω–µ —Ä–∞–∑–±–∏—Ä–∞—é—Å—å –≤ —ç—Ç–æ–º –≤–æ–ø—Ä–æ—Å–µ, –¥–∞–≤–∞–π—Ç–µ –≤–µ—Ä–Ω–µ–º—Å—è –∫ –æ–±—Å—É–∂–¥–µ–Ω–∏—é –∫–æ—Å–º–µ—Ç–æ–ª–æ–≥–∏–∏.")
        else:
            if want_services:
                if not threads[user_id]["services_loaded"]:
                    services = fetch_services(tenant_id, mydtoken)
                    threads[user_id]["services"] = services
                    threads[user_id]["services_loaded"] = True
                else:
                    services = threads[user_id]["services"]
                service_text = "\n".join(services)
                thread_obj.write(f"–°–ø–∏—Å–æ–∫ –Ω–∞—à–∏—Ö —É—Å–ª—É–≥:\n{service_text}")
            else:
                thread_obj.write("–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —Å–ø—Ä–∞—à–∏–≤–∞–µ—Ç –æ –∫–æ—Å–º–µ—Ç–æ–ª–æ–≥–∏–∏ (–Ω–µ –ø—Ä–æ —É—Å–ª—É–≥–∏).")

        thread_obj.write(question)
        run = assistant.run(thread_obj)
        result = run.wait()

        logger.info(f"–û—Ç–≤–µ—Ç –∞—Å—Å–∏—Å—Ç–µ–Ω—Ç–∞: {result.text}")
        return {"response": result.text}
    except Exception as e:
        logger.error(f"–û—à–∏–±–∫–∞: {str(e)}")
        raise HTTPException(status_code=500, detail=str(e))

